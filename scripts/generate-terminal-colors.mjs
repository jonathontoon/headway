import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

const BASE_URL = "https://terminalcolors.com";
const GOGH_THEMES_URL =
  "https://raw.githubusercontent.com/Gogh-Co/Gogh/master/data/themes.json";
const OUT_FILE = path.join(process.cwd(), "src/store/theme/defaultThemes.ts");

const TAGS = [
  { mode: "light", url: `${BASE_URL}/tags/light/` },
  { mode: "dark", url: `${BASE_URL}/tags/dark/` },
];
const INCLUDED_THEME_NAMES = [
  "ayu",
  "catppuccin",
  "everforest",
  "github",
  "gruvbox",
  "night-owl",
  "rose-pine",
  "solarized",
  "tokyo-night",
  "tomorrow",
];

function extractHex(text, key) {
  const match = text.match(
    new RegExp(`${key}\\s*=\\s*["']?(#[0-9a-fA-F]{6})["']?`),
  );
  return match ? match[1].toLowerCase() : null;
}

function extractSection(raw, header) {
  const start = raw.indexOf(header);
  if (start === -1) return "";
  const nextSection = raw.indexOf("[", start + header.length);
  return nextSection === -1 ? raw.slice(start) : raw.slice(start, nextSection);
}

function parseAlacrittyToml(raw, name, mode) {
  const background = extractHex(raw, "background");
  const foreground = extractHex(raw, "foreground");
  const normalSection = extractSection(raw, "[colors.normal]");
  const brightSection = extractSection(raw, "[colors.bright]");

  const normal = [
    "black",
    "red",
    "green",
    "yellow",
    "blue",
    "magenta",
    "cyan",
    "white",
  ].map((key) => extractHex(normalSection, key));

  const bright = [
    "black",
    "red",
    "green",
    "yellow",
    "blue",
    "magenta",
    "cyan",
    "white",
  ].map((key) => extractHex(brightSection, key));

  if (
    !background ||
    !foreground ||
    normal.some((color) => !color) ||
    bright.some((color) => !color)
  ) {
    throw new Error(`Could not parse ${name} ${mode}`);
  }

  return {
    name,
    mode,
    background,
    foreground,
    colors: [...normal, ...bright],
  };
}

function slugifyName(name) {
  return name
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\+/g, "plus")
    .replace(/&/g, "and")
    .replace(/[^a-zA-Z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .toLowerCase();
}

function normalizeGoghFamilyName(name) {
  return slugifyName(
    name.replace(
      /\s+(day|night|dark|light|dark\+|light\+|light-theme|dark-theme)$/i,
      "",
    ),
  );
}

function normalizeHex(value) {
  if (typeof value !== "string" || !/^#[0-9a-fA-F]{6}$/.test(value)) {
    return null;
  }

  return value.toLowerCase();
}

function parseGoghTheme(rawTheme) {
  const mode = rawTheme.variant;
  if (mode !== "dark" && mode !== "light") return null;

  const colors = Array.from({ length: 16 }, (_, index) => {
    const key = `color_${String(index + 1).padStart(2, "0")}`;
    return normalizeHex(rawTheme[key]);
  });
  const background = normalizeHex(rawTheme.background);
  const foreground = normalizeHex(rawTheme.foreground);
  const name = normalizeGoghFamilyName(rawTheme.name);

  if (!name || !background || !foreground || colors.some((color) => !color)) {
    return null;
  }

  return {
    name,
    mode,
    background,
    foreground,
    colors,
  };
}

async function fetchText(url) {
  const response = await fetch(url, {
    headers: {
      "user-agent": "headway-theme-generator/1.0",
    },
  });

  if (!response.ok) {
    throw new Error(`Fetch failed for ${url}: ${response.status}`);
  }

  return response.text();
}

function extractTagThemeVariants(html) {
  const variants = new Map();
  const cardPattern =
    /<article>[\s\S]*?<a href="\/themes\/([^/"]+)\/([^/"]+)\/" class="block">[\s\S]*?<\/article>/g;

  for (const match of html.matchAll(cardPattern)) {
    const [, theme, variant] = match;
    variants.set(theme, variant);
  }

  return [...variants.entries()].map(([theme, variant]) => ({
    theme,
    variant,
  }));
}

function formatTheme(theme) {
  const colors = theme.colors.map((color) => `      "${color}",`).join("\n");

  return `    ${theme.mode}: {
      name: "${theme.name}",
      mode: "${theme.mode}",
      background: "${theme.background}",
      foreground: "${theme.foreground}",
      colors: [
${colors}
      ],
    },`;
}

function formatThemeFamilies(families) {
  const body = families
    .map(([name, modes]) => {
      const entries = ["dark", "light"]
        .filter((mode) => modes.has(mode))
        .map((mode) => formatTheme(modes.get(mode)))
        .join("\n");

      return `  {
    name: "${name}",
${entries}
  },`;
    })
    .join("\n");

  return `import type { ThemeFamily } from "./types";

// Generated by scripts/generate-terminal-colors.mjs.
// Sources:
// - https://terminalcolors.com/tags/light/
// - https://terminalcolors.com/tags/dark/
// - https://raw.githubusercontent.com/Gogh-Co/Gogh/master/data/themes.json
export const DEFAULT_THEME_FAMILIES: readonly ThemeFamily[] = [
${body}
];
`;
}

const families = new Map();
const stats = {
  terminalColorsThemes: 0,
  goghThemes: 0,
  skippedGoghThemes: 0,
};

for (const tag of TAGS) {
  const tagHtml = await fetchText(tag.url);
  const variants = extractTagThemeVariants(tagHtml);

  for (const { theme, variant } of variants) {
    const downloadUrl = `${BASE_URL}/downloads/alacritty/${theme}-${variant}.toml`;
    const toml = await fetchText(downloadUrl);
    const parsed = parseAlacrittyToml(toml, theme, tag.mode);
    const modes = families.get(theme) ?? new Map();

    if (!modes.has(tag.mode)) {
      modes.set(tag.mode, parsed);
      families.set(theme, modes);
      stats.terminalColorsThemes += 1;
    }
  }
}

const goghThemes = JSON.parse(await fetchText(GOGH_THEMES_URL));

for (const rawTheme of goghThemes) {
  const parsed = parseGoghTheme(rawTheme);

  if (!parsed) {
    stats.skippedGoghThemes += 1;
    continue;
  }

  const modes = families.get(parsed.name) ?? new Map();

  if (modes.has(parsed.mode)) {
    stats.skippedGoghThemes += 1;
    continue;
  }

  modes.set(parsed.mode, parsed);
  families.set(parsed.name, modes);
  stats.goghThemes += 1;
}

const selectedFamilies = INCLUDED_THEME_NAMES.map((name) => {
  const modes = families.get(name);
  if (!modes) {
    throw new Error(`Included theme "${name}" was not found in source data`);
  }

  return [name, modes];
});

const sortedFamilies = selectedFamilies.sort(([a], [b]) => a.localeCompare(b));

await mkdir(path.dirname(OUT_FILE), { recursive: true });
await writeFile(OUT_FILE, formatThemeFamilies(sortedFamilies));

console.log(`Generated ${sortedFamilies.length} theme families in ${OUT_FILE}`);
console.log(
  `Added ${stats.terminalColorsThemes} TerminalColors palettes and ${stats.goghThemes} Gogh palettes; skipped ${stats.skippedGoghThemes} Gogh duplicates/invalid entries.`,
);
